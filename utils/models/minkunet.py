class ProjectionHEAD2(torch.nn.Module):
    def __init__(self, input_dim, output_dim):
        super(ProjectionHEAD2, self).__init__()

        self.final = ME.MinkowskiConvolution(
            input_dim,
            output_dim,
            kernel_size=1,
            bias=True,
            dimension=3)
        
        self.relu = ME.MinkowskiReLU(inplace=True)
        self.weight_initialization()

    def weight_initialization(self):
        for m in self.modules():
            if isinstance(m, ME.MinkowskiConvolution):
                ME.utils.kaiming_normal_(m.kernel, mode="fan_out", nonlinearity="relu")

            if isinstance(m, ME.MinkowskiBatchNorm):
                nn.init.constant_(m.bn.weight, 1)
                nn.init.constant_(m.bn.bias, 0)

    def forward(self, x):
        x = self.final(x)
        x = self.relu(x)
        return x


class ProjectionHead(nn.Module):
    def __init__(self, in_channels, out_channels):
        nn.Module.__init__(self)

        self.projection_head = nn.Sequential(
            nn.Linear(in_channels, out_channels),
            nn.ReLU(inplace=True),
            nn.Linear(out_channels, out_channels),
        )

        self.dropout = ME.MinkowskiDropout(p=0.4)
        self.glob_pool = ME.MinkowskiGlobalMaxPooling()

    def forward(self, x):
        # from input points dropout some (increase randomness)

        x = self.glob_pool(x)
        # project the max pooled features
        out = self.projection_head(x.F)

        return out






