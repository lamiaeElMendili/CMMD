class DomainBasicBlock(nn.Module):
    expansion = 1

    def __init__(self,
                 inplanes,
                 planes,
                 stride=1,
                 dilation=1,
                 downsample=None,
                 bn_momentum=0.1,
                 dimension=-1):
        super(DomainBasicBlock, self).__init__()
        assert dimension > 0

        self.conv1 = ME.MinkowskiConvolution(
            inplanes, planes, kernel_size=3, stride=stride, dilation=dilation, dimension=dimension)
        self.norm1 = DomainMinkowskiBatchNorm(planes, momentum=bn_momentum)
        self.conv2 = ME.MinkowskiConvolution(
            planes, planes, kernel_size=3, stride=1, dilation=dilation, dimension=dimension)
        self.norm2 = DomainMinkowskiBatchNorm(planes, momentum=bn_momentum)
        self.relu = ME.MinkowskiReLU(inplace=True)
        self.downsample = downsample

    def forward(self, x, domain_label):
        residual = x

        out = self.conv1(x)
        out = self.norm1(out, domain_label)
        out = self.relu(out)

        out = self.conv2(out)
        out = self.norm2(out, domain_label)

        if self.downsample is not None:
            out1 = self.downsample[0](x)   
            residual = self.downsample[1](out1, domain_label)      

        out += residual
        out = self.relu(out)

        return out




